def solution(s):
    braille = ("100000", "110000", "100100", "100110", "100010", "110100", "110110", "110010", "010100", "101000",
               "010110", "111000", "101100", "101110", "101010", "111100", "111110", "111010", "011100", "011110",
               "101001", "111001", "010111", "010111", "101101", "101111", "101011")
    ans_list = [""] * len(s)
    for char in s:
        if char == " ":
            ans_list.append("000000")
        else:
            index = ord(char.lower()) - ord('a')
            ans_list.append(braille[index])
    return "".join(ans_list)
